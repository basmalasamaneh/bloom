def plan_for_crop(crop, weather, previous_tasks, now_dt):
    """
    Produces 0..N tasks for one crop.
    Key fix: watering interval logic.
    """
    crop_id = crop.get("id")
    name = crop.get("name", "Plant")
    stage = crop.get("stage", "")

    water_every_days = crop.get("water_every_days", 2)  # default 2 days if not provided
    last_watered_at = crop.get("last_watered_at")  # should come from DB or user action

    tasks = []

    # Try Gemini first
    base = run_gemini_task(
        {
            "id": crop_id,
            "name": name,
            "stage": stage,
            "recent_tasks": [
                {t.get("type"): t.get("title")} for t in (previous_tasks or [])
            ],
        },
        weather,
    )
    if not base:
        base = determine_base_task(name, stage, weather)

    # ---- BIG FIX: handle water tasks by schedule ----
    if base["type"] == "water":
        # 1) If already has open water task => skip
        if has_open_task(previous_tasks, crop_id, "water"):
            return []

        # 2) If not due yet (water every N days and last watered recently) => skip watering
        if watered_recently(last_watered_at, water_every_days, now_dt):
            # Not due: return a helpful non-watering reminder instead (optional)
            next_due = compute_next_watering(last_watered_at, water_every_days)
            # You can return empty list instead; I’ll return a low-priority info task to avoid silence:
            if not has_open_task(previous_tasks, crop_id, "general"):
                info = {
                    "title": f"Watering not due for {name}",
                    "description": f"Last watered: {last_watered_at}. Next watering due around: {next_due}. Check soil only if very dry.",
                    "type": "general",
                    "priority": "low",
                    "due_in_hours": 24,
                    "reason": f"Water every {water_every_days} days; not due yet",
                }
                tasks.append(build_task(crop, info, now_dt, weather))
            return tasks

        # 3) Due: create watering task
        tasks.append(build_task(crop, base, now_dt, weather))
        return tasks

    # ---- Non-water tasks: still dedupe by open task type ----
    if has_open_task(previous_tasks, crop_id, base["type"]):
        return []

    tasks.append(build_task(crop, base, now_dt, weather))
    return tasks


def main():
    try:
        raw = sys.stdin.read()
        data = json.loads(raw or "{}")

        weather = data.get("weather", {}) or {}
        crops = data.get("crops", []) or []
        previous_tasks = data.get("previous_tasks", []) or []

        now_dt = parse_dt(data.get("now")) or datetime.now(timezone.utc)

        all_tasks = []
        for crop in crops:
            all_tasks.extend(plan_for_crop(crop, weather, previous_tasks, now_dt))

        output = {
            "tasks": all_tasks,
            "weather_used": weather,
            "weather_summary": summarize_weather(weather),
            "generated_at": now_dt.isoformat(),
        }

        sys.stdout.write(json.dumps(output))
        sys.stdout.flush()

    except Exception as e:
        sys.stdout.write(json.dumps({"error": str(e)}))
        sys.stdout.flush()


if __name__ == "__main__":
    main()
