def determine_base_task(plant_name, stage, weather):
    """
    Returns a task dict like your original determine_task() :contentReference[oaicite:1]{index=1}
    """
    stage_l = (stage or "").lower()
    temp = weather.get("temperature_c") if weather else None
    rain = weather.get("rain_mm") if weather else 0

    # Weather-based tasks
    if temp is not None:
        if temp > 32:
            return {
                "title": f"Protect {plant_name} from heat",
                "description": f"Hot spell: {temp}C. Add shade, water early morning/evening, and pause fertilizing until temps drop.",
                "type": "weather",
                "priority": "high",
                "due_in_hours": 6,
                "reason": "High temperature detected",
            }
        if temp < 8:
            return {
                "title": f"Protect {plant_name} from cold",
                "description": f"Cold night ahead: {temp}C. Cover plants, move pots indoors, and avoid late watering.",
                "type": "weather",
                "priority": "high",
                "due_in_hours": 12,
                "reason": "Cold weather detected",
            }

    if rain and rain > 2:
        return {
            "title": f"Adjust watering for {plant_name}",
            "description": f"Recent rain ({rain}mm). Reduce or skip watering today. Check top 2cm soil before watering.",
            "type": "water",
            "priority": "medium",
            "due_in_hours": 6,
            "reason": "Rain levels high",
        }

    # Stage-based tasks
    if "germination" in stage_l:
        return {
            "title": f"Soil moisture check - {plant_name}",
            "description": "Keep seed zone evenly moist; mist lightly if top 1 cm is dry, avoid puddles.",
            "type": "water",
            "priority": "medium",
            "due_in_hours": 24,
            "reason": "Germination stage",
        }

    if "seedling" in stage_l:
        return {
            "title": f"Light + moisture check - {plant_name}",
            "description": "Ensure seedlings get 6-8 hours of light; water lightly if top layer is dry.",
            "type": "care",
            "priority": "medium",
            "due_in_hours": 24,
            "reason": "Seedling stage needs monitoring",
        }

    if "vegetative" in stage_l:
        return {
            "title": f"Nutrient boost - {plant_name}",
            "description": "Apply nitrogen-rich fertilizer; keep soil evenly moist and weed-free.",
            "type": "fertilizer",
            "priority": "medium",
            "due_in_hours": 48,
            "reason": "Vegetative growth requires nutrients",
        }

    if "flowering" in stage_l:
        return {
            "title": f"Support flowers - {plant_name}",
            "description": "Avoid water stress; water in morning, gently shake stems for pollination if indoors.",
            "type": "care",
            "priority": "high",
            "due_in_hours": 48,
            "reason": "Flowering plants need stable conditions",
        }

    if "fruiting" in stage_l:
        return {
            "title": f"Fruit check - {plant_name}",
            "description": "Inspect fruits, prune yellow leaves, and deep soak every 2-3 days if soil dries.",
            "type": "care",
            "priority": "medium",
            "due_in_hours": 36,
            "reason": "Fruiting stage",
        }

    # Default fallback
    return {
        "title": f"General care - {plant_name}",
        "description": "Check soil moisture (top 2 cm), remove yellow leaves, and inspect for pests today.",
        "type": "general",
        "priority": "low",
        "due_in_hours": 48,
        "reason": "No specific condition triggered",
    }


def build_task(crop, base_task, now_dt, weather):
    due_date = now_dt + timedelta(hours=int(base_task["due_in_hours"]))
    return {
        "crop_id": crop.get("id"),
        "crop_name": crop.get("name", "Plant"),
        "stage": crop.get("stage", ""),
        "title": base_task["title"],
        "description": base_task["description"],
        "type": base_task["type"],
        "priority": base_task["priority"],
        "due_date": due_date.strftime("%Y-%m-%d %H:%M:%S"),
        "reason": base_task["reason"],
        "weather_used": weather,
        "weather_summary": summarize_weather(weather),
    }


