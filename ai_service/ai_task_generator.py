def parse_dt(s):
    """
    Accepts:
      - "2026-01-16 10:00:00"
      - "2026-01-16T10:00:00Z"
      - "2026-01-16T10:00:00+00:00"
    Returns timezone-aware datetime (UTC) when possible.
    """
    if not s:
        return None
    try:
        if "T" in s:
            return datetime.fromisoformat(s.replace("Z", "+00:00"))
        # assume UTC if naive format
        return datetime.strptime(s, "%Y-%m-%d %H:%M:%S").replace(tzinfo=timezone.utc)
    except Exception:
        return None


def summarize_weather(weather):
    if not weather:
        return ""
    try:
        temp = weather.get("temperature_c")
        cond = weather.get("conditions")
        rain = weather.get("rain_mm")
        parts = []
        if temp is not None:
            parts.append(f"{float(temp):.1f}C")
        if cond:
            parts.append(str(cond))
        if rain:
            parts.append(f"{rain}mm rain")
        return ", ".join(parts)
    except Exception:
        return ""


def has_open_task(previous_tasks, crop_id, task_type):
    """
    Return True if there is an OPEN task of same type for same crop.
    previous_tasks item example:
      { "crop_id": 1, "type": "water", "status": "open" }
    """
    for t in previous_tasks or []:
        if t.get("crop_id") != crop_id:
            continue
        if (t.get("type") or "").lower() != (task_type or "").lower():
            continue
        status = (t.get("status") or "open").lower()
        if status in ("open", "pending", "in_progress", "todo"):
            return True
    return False


def watered_recently(last_watered_at, water_every_days, now_dt):
    """
    True => NOT due yet (skip watering task)
    False => due for watering
    """
    if not water_every_days:
        return False  # if not configured, treat as due
    last_dt = parse_dt(last_watered_at)
    if not last_dt:
        return False  # unknown last watered => treat as due

    due_at = last_dt + timedelta(days=int(water_every_days))
    return now_dt < due_at


def compute_next_watering(last_watered_at, water_every_days):
    last_dt = parse_dt(last_watered_at)
    if not last_dt or not water_every_days:
        return None
    due_at = last_dt + timedelta(days=int(water_every_days))
    return due_at.isoformat()


# Gemini-powered task helper
try:
    from google import genai
except Exception:
    genai = None

GEMINI_TASK_MODEL = os.getenv("GEMINI_TASK_MODEL", "gemini-2.0-flash")
GEMINI_API_KEY = os.getenv("GEMINI_API_KEY", "")


def run_gemini_task(crop, weather):
    if not genai:
        return None
    if not GEMINI_API_KEY:
        return None

    weather_summary = summarize_weather(weather or {})
    prompt = f"""
You are a farming assistant that crafts a single actionable care task for the user.
Input:
- crop name: {crop.get("name")}
- stage: {crop.get("stage") or "unknown"}
- recent tasks: {crop.get("recent_tasks", [])}
- weather summary: {weather_summary or "none"}

Return JSON with keys: title, description, type, priority, due_in_hours, reason.
Use short sentences, focus on what to do next, and avoid asking questions.
If nothing actionable, return description 'Monitor plant and report back.' and priority 'low'.
""".strip()

    try:
        client = genai.Client(api_key=GEMINI_API_KEY)
        resp = client.models.generate_content(
            model=GEMINI_TASK_MODEL,
            contents=[{"text": prompt}],
        )
        data = json.loads(resp.text or "{}")
    except Exception as exc:
        return None

    if not data.get("title"):
        return None

    return {
        "title": data.get("title"),
        "description": data.get("description", ""),
        "type": data.get("type") or "general",
        "priority": data.get("priority") or "medium",
        "due_in_hours": int(data.get("due_in_hours") or 24),
        "reason": data.get("reason", "Generated by AI"),
        "analysis_source": "gemini",
    }


